-- Ensure required columns exist
DO $$
BEGIN
    -- Add is_active column if it doesn't exist
    IF NOT EXISTS (
        SELECT 1 FROM information_schema.columns 
        WHERE table_name = 'classification_codes' 
        AND column_name = 'is_active'
    ) THEN
        ALTER TABLE classification_codes ADD COLUMN is_active BOOLEAN DEFAULT true;
        CREATE INDEX IF NOT EXISTS idx_classification_codes_active ON classification_codes(is_active);
    END IF;
    
    -- Add is_primary column if it doesn't exist
    IF NOT EXISTS (
        SELECT 1 FROM information_schema.columns 
        WHERE table_name = 'classification_codes' 
        AND column_name = 'is_primary'
    ) THEN
        ALTER TABLE classification_codes ADD COLUMN is_primary BOOLEAN DEFAULT false;
        CREATE INDEX IF NOT EXISTS idx_classification_codes_primary ON classification_codes(is_primary);
    END IF;
    
    -- Add confidence column if it doesn't exist
    IF NOT EXISTS (
        SELECT 1 FROM information_schema.columns 
        WHERE table_name = 'classification_codes' 
        AND column_name = 'confidence'
    ) THEN
        ALTER TABLE classification_codes ADD COLUMN confidence DECIMAL(3,2) DEFAULT 0.80 
            CHECK (confidence >= 0.00 AND confidence <= 1.00);
    END IF;
END $$;

